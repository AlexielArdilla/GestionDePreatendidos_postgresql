package modelo;

public class Gestion {
	
	private int id;
	private String nom_apel;
	private String nom_tramite;
	private String fecha_y_hora;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNom_apel() {
		return nom_apel;
	}
	public void setNom_apel(String nom_apel) {
		this.nom_apel = nom_apel;
	}
	public String getNom_tramite() {
		return nom_tramite;
	}
	public void setNom_tramite(String nom_tramite) {
		this.nom_tramite = nom_tramite;
	}
	public String getFecha_y_hora() {
		return fecha_y_hora;
	}
	public void setFecha_y_hora(String fecha_y_hora) {
		this.fecha_y_hora = fecha_y_hora;
	}
	
	

}
